#include "3-calc.h"
/**
 * op_add - finds the sum of two numbers
 * @a:first number
 * @b:second number
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - find the difference between two numbers
 * @a:first number
 * @b:second number
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - finds the product of two numbers
 * @a:first number
 * @b:second number
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - finds the quotient of two numbers
 * @a:first number
 * @b:second number
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the remainder of two numbers
  * @a:first number
 * @b:second number
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
