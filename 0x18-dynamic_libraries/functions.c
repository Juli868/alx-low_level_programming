/**
 * add -calculates the sum of two numbers
 * @a: first number
 * @b: second number
 * Return: the sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - finds the difference between two numbers
 *@a: first number
 *@b: secind number
 *Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - calculates the remainder between two numbers
 * @a: first number
 * @b: second number
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul- finds the product of two numbers
 * @a: first number
 * @b: second number
 * Return: their product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - finds a quotient of two numbes
 * @a: first number
 * @b: second number
 * Return: the quotient
 */
float div(int a, int b)
{
	return (a / b);
}
